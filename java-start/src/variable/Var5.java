package variable;

public class Var5 {
	public static void main(String[] args) {
		// 변수 초기화
		// 선언만 진행
		int a;
		// a 변수 초기화
		a = 50;
		System.out.println(a);

		// 선언과 초기화 동시
		int b = 40;
		System.out.println(b);

		// 여러 변수 선언 및 초기화
		int c = 24, d = 25;
		System.out.println(c);
		System.out.println(d);

	}
}
