package variable;

public class Var1 {
	public static void main(String[] args) {
		// 10 이라는 숫자를 30으로 변경해보자.
		// System.out.println(10);
		// System.out.println(10);
		// System.out.println(10);
		/**
		 * 10 의 숫자를 30으로 바꾸기 위해서 총 3번의 변경이 일어나게 된다.
		 * 만약 이런 값이 100 ~ 100000 개가 있다면 일일이 바꾸어야 하며, 분명 누락이되는 경우도 있을 것이다.
		 */
		System.out.println(30);
		System.out.println(30);
		System.out.println(30);

		//
		/**
		 * 이런 입력 값을 누군가는 100, 200 각각 다르게 입력 할 수 있다.
		 * 그럼 입력할 때마다 변경해야한다면 굉장히 불편하다.
		 * 결국 어딘가에 값을 보관하고 필요할 때 꺼내쓰며, 보관한 값만 변경하면 알아서 데이터가 바뀌면 좋을 듯하다.
		 * 이런 역할을 하는 것이 변수(variable) 라는 기능이다.
		 */
	}
}
