package array;

public class Array1 {
	public static void main(String[] args) {
		int student1 = 90;
		int student2 = 80;
		int student3 = 70;
		int student4 = 60;
		int student5 = 50;

		System.out.println("student1 = " + student1);
		System.out.println("student2 = " + student2);
		System.out.println("student3 = " + student3);
		System.out.println("student4 = " + student4);
		System.out.println("student5 = " + student5);

		// 위와 같이 변수(학생)의 수가 늘어나게 되면 변수를 선언하고 출력하는 양을 늘려야 한다.
		// 즉, 변수가 늘어남에 따라서 코딩 양이 비례하게 된다.
		// 이러한 같은 타입의 변수를 반복해서 선언하고 반복해서 사용하는 문제를 해결하는 것이 배열이다.
	}
}
